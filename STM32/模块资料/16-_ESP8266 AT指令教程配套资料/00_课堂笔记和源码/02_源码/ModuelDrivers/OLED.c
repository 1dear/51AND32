static void OLED_WriteCmd(uint8_t cmd)
{
    I2C_Start();
    I2C_SendByte(OLED_ADDRESS);    // 从地址 + 写模式
    I2C_SendByte(0x00);         // Co + DC# + 0b000000, Co=0, DC#=0
    I2C_SendByte(cmd);         // 写数据
    I2C_Stop();
}

static void OLED_WriteData(uint8_t data)
{
    I2C_Start();
    I2C_SendByte(OLED_ADDRESS);    // 从地址 + 写模式
    I2C_SendByte(0x40);         // Co + DC# + 0b000000, Co=0, DC#=0
    I2C_SendByte(data);         // 写数据
    I2C_Stop();
}

static void OLED_WriteDatas(uint8_t *buf, uint16_t length)
{
    uint16_t i = 0;
    I2C_Start();
    I2C_SendByte(OLED_ADDRESS);    // 从地址 + 写模式
    I2C_SendByte(0x40);         // Co + DC# + 0b000000, Co=0, DC#=0
    for(i=0; i<length; i++)
    {
        I2C_SendByte(buf[i]);         // 写数据
    }
    I2C_Stop();
}


static void OLED_SetPageAddrMode(void)
{
    OLED_WriteCmd(0x20);
    OLED_WriteCmd(0x02);
}

static void OLED_SetPos(int page, int col)
{
    OLED_WriteCmd(0xB0 + page); /* page address */

    OLED_WriteCmd(col & 0xf);   /* Lower Column Start Address */
    OLED_WriteCmd(0x10 + (col >> 4));   /* Lower Higher Start Address */
}

void OLED_Clear(void)
{
    int page;
    uint8_t tmp_buf[128] = {0};
    for (page = 0; page < 8; page ++)
    {
        OLED_SetPos(page, 0);
        OLED_WriteDatas(tmp_buf, 128);
    }
}

void OLED_Init(void)
{
    /* 向OLED发命令以初始化 */
    OLED_WriteCmd(0xAE); /*display off*/ 
    OLED_WriteCmd(0x00); /*set lower column address*/ 
    OLED_WriteCmd(0x10); /*set higher column address*/ 
    OLED_WriteCmd(0x40); /*set display start line*/ 
    OLED_WriteCmd(0xB0); /*set page address*/ 
    OLED_WriteCmd(0x81); /*contract control*/ 
    OLED_WriteCmd(0x66); /*128*/ 
    OLED_WriteCmd(0xA1); /*set segment remap*/ 
    OLED_WriteCmd(0xA6); /*normal / reverse*/ 
    OLED_WriteCmd(0xA8); /*multiplex ratio*/ 
    OLED_WriteCmd(0x3F); /*duty = 1/64*/ 
    OLED_WriteCmd(0xC8); /*Com scan direction*/ 
    OLED_WriteCmd(0xD3); /*set display offset*/ 
    OLED_WriteCmd(0x00); 
    OLED_WriteCmd(0xD5); /*set osc division*/ 
    OLED_WriteCmd(0x80); 
    OLED_WriteCmd(0xD9); /*set pre-charge period*/ 
    OLED_WriteCmd(0x1f); 
    OLED_WriteCmd(0xDA); /*set COM pins*/ 
    OLED_WriteCmd(0x12); 
    OLED_WriteCmd(0xdb); /*set vcomh*/ 
    OLED_WriteCmd(0x30); 
    OLED_WriteCmd(0x8d); /*set charge pump enable*/ 
    OLED_WriteCmd(0x14); 

    OLED_SetPageAddrMode();

    OLED_Clear();
    
    OLED_WriteCmd(0xAF); /*display ON*/    
}

