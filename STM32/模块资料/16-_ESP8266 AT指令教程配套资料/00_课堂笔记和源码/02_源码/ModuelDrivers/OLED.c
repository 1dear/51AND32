void OLED_PutChar(int page, int col, char c)
{
    /* 得到字模 */
    const unsigned char *dots = oled_asc2_8x16[c - ' '];

    /* 发给OLED */
    OLED_SetPos(page, col);
    /* 发出8字节数据 */
    OLED_WriteDatas((uint8_t*)dots, 8);

    OLED_SetPos(page+1, col);
    /* 发出8字节数据 */
    OLED_WriteDatas((uint8_t*)(dots+8), 8);
}


void OLED_PutNumber(int page, int col, int num)
{
        /* 得到字模 */
    const unsigned char *dots = oled_asc2_8x16[num + 16];

    /* 发给OLED */
    OLED_SetPos(page, col);
    /* 发出8字节数据 */
    OLED_WriteDatas((uint8_t*)dots, 8);

    OLED_SetPos(page+1, col);
    /* 发出8字节数据 */
    OLED_WriteDatas((uint8_t*)(dots+8), 8);
}

/* page: 0-7
 * col : 0-127
 * 字符: 8x16象素
 */
void OLED_Print(int page, int col, char *str)
{
    int i = 0;
    while (str[i])
    {
        OLED_PutChar(page, col, str[i]);
        col += 8;
        if (col > 127)
        {
            col = 0;
            page += 2;
        }
        i++;
    }
}

void OLED_PrintNumber(int page, int col, uint8_t num)
{
    uint8_t uint = 0, decade = 0, hundred = 0;
    uint = (num%100%10);
    decade = (num%100/10);
    hundred = num/100;
    
    if(hundred != 0)
    {
        OLED_PutNumber(page, col, hundred);
    }
    if( (hundred != 0) ||  (decade != 0))
    {
        OLED_PutNumber(page, col + 8, decade);
    }
    
    if( (hundred != 0) ||  (decade != 0) || (uint != 0))
    {
        OLED_PutNumber(page, col + 16, uint);
    }
}

